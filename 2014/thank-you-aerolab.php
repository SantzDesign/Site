            <p><b>T</b>his open letter is dedicated to Alejandro Vizio, Augustín Linenberg, and Roberto González, and the other members of the <a href="https://aerolab.co" target="_blank">Aerolab</a> team. Thank you for what undoubtedly was an unforgettable work experience.</p>
            <p>Aerolab was my first job. I had previously done a 3-months internship at an online marketing company (work I owed to my father) but it was clearly not the same. I got the job at Aerolab by my own means. No referrals or personal/professional connections in between, and furthermore, unfolding a humble portfolio, which at best, revealed potential and zeal to learn. So thank you for the confidence and the opportunity.</p>
            <p>Months later I found out that I got hired because on the introductory email, I claimed having achieved a 95% score on Google Page Speed ​​Insights, for the website I had back then (my personal site let's say). I used to be very interested in everything that had to do with Page Speed ​​and Search Engine Optimization. Don't get me wrong... I still am, but today this matters are something I usually pull off more mechanically.</p>
            <h2>My 1st development</h2>
            <p>My first project as a front-end developer at Aerolab was the development of a video landing page for FunTime, a travel agency from Argentina. I mainly dealt with the front, but under Robert's guidance, we also handled the integration to CodeIgniter.</p>
            <p>As a beginner I took some welcoming classes with Robert, because apart of being co-founder and CTO, he was (and still is) an excellent teacher. For the duration of the project, we reviewed the basics of HTML/CSS, best practices, general dos and don'ts, and even made room for a short introduction to jQuery.</p>
            <p>After this first task, projects never stopped to get more and more interesting... Along came clients like 123Seguro, Mazda, the Motorcycle Safety Advisory Council (MSAC) of New Zealand, Terminá la Secundaria (an initiative launched by the Government of the City of Buenos Aires), AiOTRA, Ibérica Restaurants, 8Coupons, among others. There were some very cool side projects too, among them Jukebox and Aeroleague.</p>
            <h2>The end</h2>
            <p>While half the team dealt with the JSConf hosted on Argentina, the other half worked hard with Aerolab's rebrand and the website's redesign. I was in charge of developing the new website's UI... It was my last assignment at Aerolab, after almost two years.</p>
            <p>It was an excellent job: super thorough and consequence of team effort. We were at least 10 involved. Design, development, logistics, and copy.</p>
            <p>The work enclosed everything I had learned over the past two years. I was really honored I was one of the entrusted to accomplish a task that needed to be as perfect as possible.</p>
            <h2>A final thank you...</h2>
            <p>Without further ado, thank you for the time, experience and opportunity. Everything meant, means and will mean a lot to me both, personally and professionally.</p>
